# Exemplo de vetor em Python
vetor = [1, 2, 3, 4, 5]

# Imprimindo o vetor
print("Vetor:", vetor)

# Acessando um elemento do vetor (índice 2)
print("Elemento no índice 2 do vetor:", vetor[2])

# Exemplo de matriz em Python
matriz = [[1, 2, 3],
          [4, 5, 6],
          [7, 8, 9]]

# Imprimindo a matriz
print("Matriz:")
for linha in matriz:
    print(linha)

# Acessando um elemento da matriz (linha 1, coluna 2)
print("Elemento na linha 1, coluna 2 da matriz:", matriz[1][2])
